public class relational_op {
    public static void main(String[] args) {
        int a = 25;
        int b = 63;
        boolean c = true;
        boolean d = false;
        int e = a+b*a;

        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(c||d);
        System.out.println(c&&d);
        System.out.println(e);

    }
}
