public class while_loop {
    public static void main(String[] args)
    {
        System.out.println("Using for loop");
        for(int i=1;i<10;i++)
        System.out.println(i);

        System.out.println("Using do while loop");
        int j = 1;
        do
            {
                System.out.println(j);
                j++;
            } while(j<10);
        }
    }

